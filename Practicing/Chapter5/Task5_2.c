#define _CRT_SECURE_NO_WARNINGS // MSVC 컴파일러에서 안전하지 않은 함수 사용 경고를 무시한다.
#include <stdio.h>  // 표준 입출력 함수들을 사용하기 위한 헤더 파일
#include <stdlib.h> // rand(), srand() 같은 난수 함수들이 정의된 헤더 파일
#include <time.h>   // time() 함수 사용을 위해 필요한 헤더 파일

int main() {
    
    // 1. 프로그램 시작 메시지 출력
    printf("동전 던지기 게임을 시작합니다.\n");

    // 2. srand() 함수에 현재 시간을 전달하여 난수의 씨앗(seed)을 설정
    srand(time(NULL));  
    // - `srand()`는 난수 생성을 초기화하는 함수이다.
    // - 같은 시드(seed) 값이 주어지면 난수 생성기는 항상 같은 난수 시퀀스를 반환한다.
    // - `time(NULL)`을 통해 현재 시간을 초 단위로 가져와 매번 다른 시드를 제공한다.
    // - `NULL`은 time() 함수의 인자로 현재 시간을 반환하도록 설정하기 위해 사용된다.

    // 3. rand() 함수로 난수 생성 후, 2로 나눈 나머지를 이용해 앞면/뒷면 결정
    int coin = rand() % 2; 
    // - `rand()` 함수는 0에서 `RAND_MAX` 사이의 난수를 반환한다.
    // - `% 2`는 난수를 2로 나눈 나머지를 계산해 **0 또는 1**의 값을 얻는다.
    //   - 0: 앞면, 1: 뒷면
    // - 이렇게 간단한 난수 생성으로 동전의 앞면 또는 뒷면을 결정할 수 있다.

    // 4. 조건문을 사용해 결과 출력
    if (coin == 0) printf("앞면입니다.\n");  // 난수 결과가 0이면 앞면 출력
    else printf("뒷면입니다.\n");  // 난수 결과가 1이면 뒷면 출력

    // 5. 프로그램 종료
    return 0;
}